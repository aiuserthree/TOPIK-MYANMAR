def _title_row(ws, text: str, ncol: int, row: int = 2) -> None:
    ws.cell(row, 2, value=text).font = TITLE_FONT
    ws.cell(row, 2).alignment = Alignment(vertical="center", horizontal="left")
    ws.row_dimensions[row].height = 26


def _header_row(ws, headers: list[str], row: int = 5, start_col: int = 2) -> None:
    for i, h in enumerate(headers):
        c = ws.cell(row, start_col + i, value=h)
        c.font = HDR_FONT
        c.fill = HDR_FILL
        c.border = THIN
        c.alignment = Alignment(vertical="center", horizontal="center", wrap_text=True)
    ws.row_dimensions[row].height = 32


def _data_rows(ws, rows: list[tuple], row_start: int = 6, start_col: int = 2,
               page_no_idx: int = 0) -> None:
    for i, tup in enumerate(rows):
        r = row_start + i
        kind = _row_kind(str(tup[page_no_idx] or ""))
        fill = DEPTH_FILLS.get(kind, DEPTH_FILLS["P"])
        for j, val in enumerate(tup):
            c = ws.cell(r, start_col + j, value=val)
            c.border = THIN
            c.alignment = Alignment(vertical="top", wrap_text=True)
            c.fill = fill
        ws.cell(r, start_col).font = Font(bold=(kind == "P"))
        line_count = max(1, str(tup[-2]).count("\n") + 1)
        ws.row_dimensions[r].height = min(420, max(28, line_count * 16))


def _set_widths(ws, widths: list[int], start_col: int = 2) -> None:
    for i, w in enumerate(widths):
        ws.column_dimensions[get_column_letter(start_col + i)].width = w
    ws.column_dimensions["A"].width = 3


def _build_menu_tree_sheet(ws, tree: list[tuple], title: str) -> None:
    _title_row(ws, title, ncol=12)
    ws.cell(5, 2, value="1 Depth").font = HDR_FONT
    ws.cell(5, 2).fill = HDR_FILL
    ws.cell(5, 2).border = THIN
    ws.cell(5, 2).alignment = Alignment(horizontal="center", vertical="center")
    ws.cell(5, 3, value="2~3 Depth (하위 메뉴)").font = HDR_FONT
    ws.cell(5, 3).fill = HDR_FILL
    ws.cell(5, 3).border = THIN
    ws.cell(5, 3).alignment = Alignment(horizontal="center", vertical="center")
    ws.row_dimensions[5].height = 26

    r = 6
    for d1, items in tree:
        ws.cell(r, 2, value=d1).font = Font(bold=True)
        ws.cell(r, 2).fill = DEPTH_FILLS["P"]
        ws.cell(r, 2).border = THIN
        ws.cell(r, 2).alignment = Alignment(vertical="top", wrap_text=True)
        ws.cell(r, 3, value="\n".join(items))
        ws.cell(r, 3).fill = DEPTH_FILLS["S"]
        ws.cell(r, 3).border = THIN
        ws.cell(r, 3).alignment = Alignment(vertical="top", wrap_text=True)
        ws.row_dimensions[r].height = min(400, max(24, len(items) * 18))
        r += 1

    ws.column_dimensions["A"].width = 3
    ws.column_dimensions["B"].width = 26
    ws.column_dimensions["C"].width = 90


def build_fo_workbook(path: Path) -> None:
    wb = openpyxl.Workbook()
    ws = wb.active
    ws.title = "FO_IA"
    _title_row(ws, "주미얀마대사관 한국어능력시험(TOPIK) 웹사이트 — FO IA(메뉴 구성 v1.0 · 고객사 0519 반영)", ncol=13)
    _header_row(ws, FO_HEADERS)
    _data_rows(ws, FO_ROWS)
    widths = [22, 8, 14, 18, 22, 22, 28, 6, 14, 8, 56, 38, 30]
    _set_widths(ws, widths)
