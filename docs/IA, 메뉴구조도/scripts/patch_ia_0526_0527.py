def apply_updates(path, sheet_index, desc_col, updates):
    wb = openpyxl.load_workbook(path)
    ws = wb.worksheets[sheet_index]
    updated = []
    for row_num, new_val in updates.items():
        cell = ws.cell(row=row_num, column=desc_col)
        old_val = cell.value
        cell.value = new_val
        page_no = ws.cell(row=row_num, column=2).value
        updated.append((row_num, page_no))
        print(f"  [row{row_num}] {page_no}")
        print(f"    이전: {str(old_val)[:80]}...")
        print(f"    이후: {str(new_val)[:80]}...")
        print()
    wb.save(path)
    return len(updated)


