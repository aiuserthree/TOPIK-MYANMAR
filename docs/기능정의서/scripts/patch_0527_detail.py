def patch_cell_values(ws, updates: dict[str, str]) -> int:
    """B열(컬럼 2) 값이 updates 키와 일치하는 셀을 값으로 교체."""
    count = 0
    for row in ws.iter_rows(min_row=2):
        b_cell = row[1]
        val = str(b_cell.value or "").strip()
        if val in updates:
            b_cell.value = updates[val]
            _style_body(b_cell)
            count += 1
    return count


