# ── 스타일 ──────────────────────────────────────────────────────────────────
THIN = Side(style="thin", color="C5CDD9")
BOX = Border(left=THIN, right=THIN, top=THIN, bottom=THIN)
SECTION_FILL = PatternFill(start_color="FFF2CC", end_color="FFF2CC", fill_type="solid")
SECTION_FONT = Font(bold=True, color="7B5C00", size=11)
WRAP_TOP = Alignment(wrap_text=True, vertical="top")
STRIKE_FONT = Font(strike=True, color="999999")


def _style_section(cell):
    cell.font = SECTION_FONT
    cell.fill = SECTION_FILL
    cell.alignment = WRAP_TOP
    cell.border = BOX


def _style_body(cell):
    cell.alignment = WRAP_TOP
    cell.border = BOX


def append_section(ws, section_title: str, lines: list[str]) -> None:
    """상세설명 시트 맨 아래에 빈 행 + 새 섹션 추가."""
    max_row = ws.max_row

    # 빈 구분 행
    ws.cell(max_row + 1, 1, value=None).border = BOX
    ws.cell(max_row + 1, 2, value=None).border = BOX

    r = max_row + 2
    first = True
    for line in lines:
        if first:
            a = ws.cell(r, 1, value=section_title)
            _style_section(a)
            first = False
        else:
            a = ws.cell(r, 1, value=None)
            a.border = BOX
        b = ws.cell(r, 2, value=line)
        _style_body(b)
        r += 1


