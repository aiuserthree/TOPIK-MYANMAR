def main() -> None:
    print("=" * 60)
    print("고객사 0527 — 상세설명 시트 패치 시작")
    print("=" * 60)

    tasks = [
        # (파일 경로, 기존 셀 수정 dict, 추가 섹션 라인)
        (FO_DIR / "02_TOPIK안내_기능정의서.xlsx",       None,           _FO02_LINES),
        (FO_DIR / "04_TOPIK접수_기능정의서.xlsx",        _FO04_UPDATES,  _FO04_LINES),
        (FO_DIR / "06_계정_회원가입·로그인·내정보_기능정의서.xlsx", _FO06_UPDATES, _FO06_LINES),
        (BO_DIR / "02_접수관리_기능정의서.xlsx",          None,           _BO02_LINES),
        (BO_DIR / "04_콘텐츠관리_기능정의서.xlsx",        None,           _BO04_LINES),
    ]

    for path, cell_updates, section_lines in tasks:
        print(f"\n[{path.name}]")
        if not path.is_file():
            print(f"  [SKIP] 파일 없음")
            continue

        wb = load_workbook(path)
        if "상세설명" not in wb.sheetnames:
            print(f"  [SKIP] 상세설명 시트 없음")
            continue

        ws = wb["상세설명"]

        # ① 기존 셀 수정
        if cell_updates:
            n = patch_cell_values(ws, cell_updates)
            print(f"  ① 기존 셀 수정: {n}셀")

        # ② 섹션 추가
        append_section(ws, "고객사 수정 이력 (0527)", section_lines)
        print(f"  ② 0527 이력 섹션 추가 ({len(section_lines)}행)")

        wb.save(path)
        print(f"  → 저장 완료: {path.name}")

    print(f"\n{'=' * 60}")
    print("고객사 0527 상세설명 패치 완료.")
    print("=" * 60)


if __name__ == "__main__":
    main()
