THIN = Side(style="thin", color="C5CDD9")
BOX = Border(left=THIN, right=THIN, top=THIN, bottom=THIN)
HDR_FILL = PatternFill(start_color="003478", end_color="003478", fill_type="solid")
HDR_FONT = Font(bold=True, color="FFFFFF", size=11)
TITLE_FONT = Font(bold=True, size=14, color="003478")
WRAP_TOP = Alignment(wrap_text=True, vertical="top")
WRAP_CENTER = Alignment(wrap_text=True, vertical="center", horizontal="center")
COL_WIDTHS = [9, 11, 14, 16, 16, 11, 26, 28, 8, 6, 16, 96, 24, 32]


def style_spec_header(ws, title: str) -> None:
    ws.cell(1, 1, value=f"{title} — 기준: 요구사항·IA v1.0(확장)").font = TITLE_FONT
    ws.merge_cells(start_row=1, start_column=1, end_row=1, end_column=len(HEADER_ROW))
    ws.cell(1, 1).alignment = Alignment(vertical="center", horizontal="left")
    ws.row_dimensions[1].height = 26
    for ci, h in enumerate(HEADER_ROW, start=1):
        c = ws.cell(2, ci, value=h)
        c.fill = HDR_FILL
        c.font = HDR_FONT
        c.alignment = WRAP_CENTER
        c.border = BOX
    ws.row_dimensions[2].height = 26
    for i, w in enumerate(COL_WIDTHS, start=1):
        ws.column_dimensions[get_column_letter(i)].width = w
    ws.freeze_panes = "A3"


def write_data_rows(ws, tuples) -> None:
    for off, tup in enumerate(tuples, start=3):
        for ci, val in enumerate(tup, start=1):
            c = ws.cell(off, ci, value=val)
            c.alignment = WRAP_TOP
            c.border = BOX
        desc = tup[11] if len(tup) > 11 else ""
        line_count = max(1, str(desc).count("\n") + 1)
        ws.row_dimensions[off].height = min(900, max(72, line_count * 14))


def write_detail_sheet(ws, blocks: list[tuple[str, str]]) -> None:
    """blocks: [(섹션명, 본문 텍스트), ...] — 행 단위로 (섹션, 라인번호, 내용) 기록."""
    headers = ["섹션", "라인", "내용"]
    for ci, h in enumerate(headers, start=1):
        c = ws.cell(1, ci, value=h)
        c.fill = HDR_FILL
        c.font = HDR_FONT
        c.alignment = WRAP_CENTER
        c.border = BOX
    ws.row_dimensions[1].height = 24
    ws.column_dimensions["A"].width = 18
    ws.column_dimensions["B"].width = 6
    ws.column_dimensions["C"].width = 130
    ws.freeze_panes = "A2"

    row = 2
    for section, body in blocks:
        c0 = ws.cell(row, 1, value=section)
        c0.font = Font(bold=True, color="003478")
        c0.alignment = WRAP_TOP
        c0.border = BOX
        ws.cell(row, 2, value="").border = BOX
        ws.cell(row, 3, value=section).font = Font(bold=True)
        ws.cell(row, 3).border = BOX
        ws.cell(row, 3).alignment = WRAP_TOP
        row += 1
        for idx, line in enumerate(body.split("\n"), start=1):
            ws.cell(row, 1, value="").border = BOX
            ws.cell(row, 2, value=idx).alignment = WRAP_CENTER
            ws.cell(row, 2).border = BOX
            cc = ws.cell(row, 3, value=line)
            cc.alignment = WRAP_TOP
            cc.border = BOX
            row += 1


def build_workbook(title: str, rows: list[tuple], detail_blocks: list[tuple[str, str]], out_path: Path) -> None:
    wb = Workbook()
    ws_main = wb.active
    ws_main.title = "기능정의"
    style_spec_header(ws_main, title)
    write_data_rows(ws_main, rows)

    ws_detail = wb.create_sheet("상세설명")
    write_detail_sheet(ws_detail, detail_blocks)

    wb.save(out_path)
    print(f"OK {out_path.relative_to(ROOT)} ({len(rows)}행)")


