def make_workbook() -> None:
    from collections import Counter, defaultdict

    # 영역 → 항목 리스트 매핑
    cat_map: dict[str, list] = defaultdict(list)
    for entry in CHECKLIST:
        cat_map[entry[0]].append(entry)

    wb = Workbook()
    wb.remove(wb.active)  # 기본 Sheet 제거

    # ── INDEX 시트 ────────────────────────────────────────────────────────────
    ws_index = wb.create_sheet(title="INDEX")
    sheet_stats = []

    for sheet_name, tab_color, cat_list in SHEET_GROUPS:
        items = []
        for cat in cat_list:
            items.extend(cat_map.get(cat, []))
        pris = [e[3] for e in items]
        cnt_h = sum(1 for p in pris if PRI_H in p)
        cnt_m = sum(1 for p in pris if PRI_M in p)
        cnt_l = sum(1 for p in pris if PRI_L in p)
        sheet_stats.append((sheet_name, tab_color, cnt_h, cnt_m, cnt_l, len(items)))

    write_index_sheet(ws_index, sheet_stats)

    # ── 각 영역별 시트 생성 ───────────────────────────────────────────────────
    global_seq = 1
    for sheet_name, tab_color, cat_list in SHEET_GROUPS:
        items = []
        for cat in cat_list:
            items.extend(cat_map.get(cat, []))

        if not items:
            continue

        ws = wb.create_sheet(title=sheet_name)
        apply_sheet_header(ws, tab_color)
        global_seq = write_rows(ws, items, start_seq=global_seq)

    # ── 범례 시트 ─────────────────────────────────────────────────────────────
    ws_legend = wb.create_sheet(title="범례·사용법")
    write_legend_sheet(ws_legend)

    wb.save(OUT_FILE)

    export_markdown(sheet_stats)

    # ── 통계 출력 ─────────────────────────────────────────────────────────────
    cats  = [c[0] for c in CHECKLIST]
    pris  = [c[3] for c in CHECKLIST]
    total = len(CHECKLIST)
    cnt_h = sum(1 for p in pris if PRI_H in p)
    cnt_m = sum(1 for p in pris if PRI_M in p)
    cnt_l = sum(1 for p in pris if PRI_L in p)

    print(f"✅ 저장 완료: {OUT_FILE}")
    print(f"   총 체크 항목: {total}개  (HIGH {cnt_h} / MED {cnt_m} / LOW {cnt_l})")
    print(f"   생성된 시트: {len(SHEET_GROUPS) + 2}개 (INDEX + {len(SHEET_GROUPS)}개 영역 + 범례)")
    print()
    print("📄 시트별 항목 수:")
    for sname, _, ch, cm, cl, tot in sheet_stats:
        print(f"   [{sname}]  HIGH {ch} / MED {cm} / LOW {cl}  = {tot}개")
    print()
    print("📊 영역별 항목 수:")
    for k, v in sorted(Counter(cats).items()):
        print(f"   {k}: {v}개")


if __name__ == "__main__":
    make_workbook()
