def write_rows(ws, items: list[tuple], start_seq: int = 1) -> int:
    prev_cat, prev_mod = None, None
    seq = start_seq

    for row_idx, (cat, mod, item, pri, memo) in enumerate(items, start=2):
        cat_color, mod_color = get_cat_colors(cat)

        if cat != prev_cat:
            row_fill = make_fill(cat_color)
            row_font_color = "FFFFFF"
            is_cat_row = True
        elif mod != prev_mod:
            row_fill = make_fill(mod_color)
            row_font_color = "000000"
            is_cat_row = False
        else:
            row_fill = make_fill("FFFFFF")
            row_font_color = "000000"
            is_cat_row = False

        ws.row_dimensions[row_idx].height = 28
        vals = [seq, cat, mod, item, pri, memo, "□", "", ""]

        for ci, val in enumerate(vals, 1):
            cell = ws.cell(row=row_idx, column=ci, value=val)
            cell.border = make_border()
            cell.alignment = Alignment(
                horizontal="left" if ci in (4, 6) else "center",
                vertical="center", wrap_text=True,
            )

            if is_cat_row and ci in (1, 2, 3):
                cell.fill = make_fill(cat_color)
                cell.font = Font(bold=True, color="FFFFFF", size=10)
            elif mod != prev_mod and ci == 3 and not is_cat_row:
                cell.fill = make_fill(mod_color)
                cell.font = Font(bold=True, color="000000", size=10)
            else:
                cell.fill = row_fill
                cell.font = Font(bold=is_cat_row, color=row_font_color, size=10)

            if ci == 5:
                if PRI_H in str(val):
                    cell.font = Font(bold=True, color="C62828", size=10)
                elif PRI_M in str(val):
                    cell.font = Font(bold=True, color="E65100", size=10)
                elif PRI_L in str(val):
                    cell.font = Font(bold=True, color="1B5E20", size=10)

            if ci == 6 and "⚠️" in str(val):
                cell.font = Font(bold=True, color="B71C1C", size=10)

            if ci == 7:
                cell.font = Font(color="555555", size=11)
                cell.alignment = Alignment(horizontal="center", vertical="center")

        prev_cat, prev_mod = cat, mod
        seq += 1

    return seq  # 다음 시트의 NO 시작값


